QA = """
═══════════════════════════════════════════════════════════════
Q1: BM25和稠密向量检索有什么区别？为什么都用？
───────────────────────────────────────────────────────────
A: 稠密向量检索（Dense Embedding）把文本转为语义向量，能匹配同义词
   ——"解除合同"和"合同终止"语义相近所以向量距离近。但它对专有名词
   和精确关键词不敏感。

   BM25是基于词频的稀疏检索，核心是TF-IDF的改进版。它精确匹配关键词，
   但无法理解语义。

   两者互补。所以我们用双路检索：稠密抓语义、稀疏抓精确命中，然后
   用RRF融合两路结果，再经过Cross-Encoder精排。

═══════════════════════════════════════════════════════════════
Q2: RRF的k值为什么选60？换成30会怎样？
───────────────────────────────────────────────────────────
A: k是RRF公式中的常数：score = sum(1/(k + rank))

   k越小，排名靠前的结果权重越大。k=60是原始论文推荐的默认值，
   平衡了头部和尾部结果的贡献。

   如果k=30，前几名的权重会更大，适合检索质量已经很高、只需要微调
   的场景。如果k=100，排名影响变小，结果更接近两路检索的原始分布。

═══════════════════════════════════════════════════════════════
Q3: Cross-Encoder和Bi-Encoder的区别是什么？
───────────────────────────────────────────────────────────
A: Bi-Encoder（如稠密向量模型）对query和doc分别编码成向量，然后
   算余弦相似度。速度快（可以预计算doc向量），但精度有限。

   Cross-Encoder把query和doc拼在一起输入模型，直接输出相似度分数。
   精度高，但慢——每次查询都要重新计算所有候选对。

   我们在实践中：先用Bi-Encoder + BM25做第一轮召回（速度快），然后
   用Cross-Encoder对Top-30~50的候选做精排。这样兼顾了速度和精度。

═══════════════════════════════════════════════════════════════
Q4: 分块大小为什么选512？Overlap为什么是128？
───────────────────────────────────────────────────────────
A: Chunk Size的选择取决于检索粒度和语义完整性的平衡。

   512 token的考虑：
   - 太短（128）：语义不完整，一句话被切到两个块里，检索时都匹配不全
   - 太长（1024+）：一个块包含多个主题，检索引擎难以精确定位
   - 512是经验值，适合大部分中文文档场景

   Overlap 128（25%）：
   - 块与块之间的重叠，避免恰好在关键句处切断
   - 25%是常用值，覆盖段落边界

   我们后来用这个配置做了A/B测试，对比256/512/1024三种Size，
   512在ContextRecall上表现最好。

═══════════════════════════════════════════════════════════════
Q5: RAGAS的三维度和具体怎么算的？
───────────────────────────────────────────────────────────
A:
   1. Faithfulness（忠实度）：衡量回答是否基于检索到的上下文
      → 把回答拆成一个个claim（声明句），逐个判断是否被上下文支持
      → score = 被支持的claim数 / 总claim数

   2. AnswerRelevancy（回答相关性）：衡量回答是否针对问题
      → 从回答反向生成问题，看与原始问题的相似度
      → 回答跑题时分数低

   3. ContextRecall（上下文召回率）：衡量检索到的内容是否覆盖了
      ground truth所需的信息
      → 把ground truth拆成claims，逐个判断是否出现在检索结果中

   评估用的LLM Judge是DeepSeek（通过配置OPENAI_API_KEY指向DeepSeek地址）

═══════════════════════════════════════════════════════════════
Q6: Faithfulness从0.62提升到0.81，具体做了什么？
───────────────────────────────────────────────────────────
A: 三条线并行：

   1. 检索质量提升（贡献最大）：
      - 纯稠密 → BM25+Dense双路 + RRF融合
      - 加Cross-Encoder重排序
      - 效果：Top-10命中率+25%，相关上下文更多了

   2. 提示词约束：
      - System Prompt明确写"只基于资料回答，不足时说明无法回答"
      - 要求回答中标注引用编号[1][2]，强制LLM对照原文

   3. 上下文质量控制：
      - 加分数阈值过滤（min_score=0.2），低质量结果直接丢掉
      - 调整Chunk Size从400→512，Overlap从50→128

   每次改动后跑31道Golden Test Set做回归测试，确保不退化。

═══════════════════════════════════════════════════════════════
Q7: 多模态检索是怎么实现的？
───────────────────────────────────────────────────────────
A: 多模态指的是PDF/文档中的图片也能被检索到。

   过程：
   1. PyMuPDF提取文档中的图片
   2. Vision LLM（通过API调用DeepSeek-VL）对图片生成文字描述（Caption）
   3. 把Caption缝合到该页的文本Chunk中
   4. 用户检索时，通过Caption匹配到图片

   这样用户搜"收入趋势图"就能找到图表图片，实现"搜文字出图"。

   兜底方案：如果Vision LLM API不可用，用OCR提取图片中的文字作为
   备选描述。

═══════════════════════════════════════════════════════════════
Q8: 这个项目遇到的最大技术难点是什么？
───────────────────────────────────────────────────────────
A: 最大的难点是Golden Test Set的设计。

   一开始我们让业务方直接写QA对做测试集，但不同人写的ground truth
   标准不一致——有的人写得很简略，有的人写得很详细，导致RAGAS评分
   波动巨大，无法判断优化到底是有效还是无效。

   后来我们统一了模板：每个问题必须包含——
   - 问题（question）
   - 期望答案（ground_truth）  
   - 来源文档和段落标注
   - 难度分类（easy/medium/hard）

   这样Faithfulness评估才稳定下来。这个经验告诉我们，评估体系的
   设计比优化本身更重要——标准不统一，再好的优化也看不出来。

═══════════════════════════════════════════════════════════════
Q9: 线上如果出现检索质量下降怎么办？
───────────────────────────────────────────────────────────
A: 我们的可观测性系统起到关键作用。

   TraceContext记录了每次查询的全链路数据：
   - 各阶段耗时（改写→检索→生成）
   - 检索结果数量和质量
   - Token消耗
   - 异常信息

   出问题时：
   1. 查Trace日志，定位哪个环节异常
   2. 对比最近的Golden Test Set回归结果，看哪些指标下降
   3. 如果是检索问题，检查BM25索引是否需要重建
   4. 如果是LLM问题，检查API状态和Prompt

═══════════════════════════════════════════════════════════════
Q10: 这个项目跟市面上开源的RAG框架（如LangChain、LlamaIndex）
     有什么区别？为什么不直接用？
───────────────────────────────────────────────────────────
A: LangChain和LlamaIndex提供了基础组件，但我们在三个层面做了
   定制化：

   1. 检索策略：开源框架的默认Retriever只做单路稠密检索。我们自己
      实现了BM25+Dense双路 + RRF融合 + Cross-Encoder重排序。

   2. 评估体系：大多数RAG项目没有系统性的评估。我们基于RAGAS搭建了
      完整的评估管线，31道Golden Test Set + 回归测试。

   3. 多模态：LangChain的文档加载器不支持Vision LLM生成Caption。
      我们自己实现了图片提取→Vision LLM描述→缝合Chunk的管线。

   用LangChain做基础设施，在上面搭建自己的业务逻辑——比完全自研
   省时间，比直接用默认配置效果好。
"""


