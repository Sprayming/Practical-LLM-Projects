CONCEPTS = """
═══════════════════════════════════════════════════════════════
BM25
  基于词频的检索算法，TF-IDF的改进版。
  核心公式：score = IDF * (k1+1)*tf / (k1*(1-b+b*dl/avgdl) + tf)
  k1控制词频饱和度（默认1.2~2.0），b控制文档长度归一化（默认0.75）

RRF (Reciprocal Rank Fusion)
  融合多个排序结果的算法。
  公式：score(d) = sum(1 / (k + rank_i(d)))
  不看具体分数，只看排名位置。k=60是推荐默认值。

Cross-Encoder
  把query和doc拼一起输入，输出相似度分数。精度高但慢。
  适用场景：对Top-N候选做精排（N通常30~50）

Bi-Encoder
  query和doc分别编码成向量，余弦相似度。速度快可预计算。
  适用场景：第一轮召回（候选集通常100~1000）

Chunk Size
  文本分块的token数。512是经验值。
  太小：语义不完整；太大：一个块含多个主题，检索不精确

Chunk Overlap
  块之间重叠的token数。通常取Chunk Size的20~25%。
  避免关键句恰好在切分边界被切断

RAGAS
  RAG评估框架，核心指标：
  - Faithfulness：回答是否忠于上下文（有没有编造）
  - AnswerRelevancy：回答是否针对问题
  - ContextPrecision：检索到的内容有多少是相关的
  - ContextRecall：该召回的相关内容是否都召回了

RRF k值
  控制RRF对排名位置的敏感度。k越小前几名权重越大。
  k=60: 平衡；k=30: 强调头部；k=100: 更均匀
"""


